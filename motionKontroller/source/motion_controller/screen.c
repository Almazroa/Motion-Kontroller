void display_message(unsigned char *message)
{
	// comm_out(0x40);		// start lines
	// while (*message)
	// {
	// 	data_out(*message);
	// 	message++;
	// }

	unsigned char page = PAGE_ADDR;
	comm_out(0xAE); // Display OFF
	comm_out(0x40); // Display start address + 0x40
	for (unsigned int i = 0; i < 4; i++)
	{                   // 32pixel display / 8 pixels per page = 4 pages
		comm_out(page); // send page address
		comm_out(0x10); // column address upper 4 bits + 0x10
		comm_out(0x00); // column address lower 4 bits + 0x00
		for (unsigned int j = 0; j < 128; j++)
		{                          // 128 columns wide
		data_out(*message); // send picture data
		message++; 
		}
		page++; // after 128 columns, go to next page
	}
	comm_out(0xAF);

}

void lcd_clear(void)
{

	unsigned char page = PAGE_ADDR;
	comm_out(0xAE); // Display OFF
	comm_out(0x40); // Display start address + 0x40
	for (unsigned int i = 0; i < 4; i++)
	{                   // 32pixel display / 8 pixels per page = 4 pages
		comm_out(page); // send page address
		comm_out(0x10); // column address upper 4 bits + 0x10
		comm_out(0x00); // column address lower 4 bits + 0x00
		for (unsigned int j = 0; j < 128; j++)
		{                          // 128 columns wide
		data_out(0x00); // send picture data
		}
		page++; // after 128 columns, go to next page
	}
	comm_out(0xAF);
}

